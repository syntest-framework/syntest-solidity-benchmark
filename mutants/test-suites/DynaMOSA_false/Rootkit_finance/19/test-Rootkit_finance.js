const Rootkit_finance = artifacts.require("Rootkit_finance");
const chai = require('chai');
const expect = chai.expect;
chai.use(require('chai-as-promised'));

contract('Rootkit_finance', (accounts) => {
	it('test for Rootkit_finance', async () => {
		const Rootkit_financejIkGDOp = await Rootkit_finance.new({from: "0x0000000000000000000000000000000000000001"});
		const uintG5CGmYt = BigInt("2012")
		const addressBreREHz = accounts[4]
		const addressgL36BV3 = accounts[3]
		const addressRXPm8XO = "0x0000000000000000000000000000000000000001"
		const uintob7s0D = BigInt("1759")
		const addressmZoC8Y = accounts[5]
		const addressGc1xmjk = accounts[4]
		const boolergsY9k = await Rootkit_financejIkGDOp.transferFrom.call(addressgL36BV3, addressBreREHz, uintG5CGmYt, {from: accounts[4]});
		const uint256FXi50H9 = await Rootkit_financejIkGDOp.balanceOf.call(addressRXPm8XO, {from: accounts[1]});
		const booloWITroS = await Rootkit_financejIkGDOp.transferFrom.call(addressGc1xmjk, addressmZoC8Y, uintob7s0D, {from: accounts[2]});
	});

	it('test for Rootkit_finance', async () => {
		const Rootkit_financexifbpsk = await Rootkit_finance.new({from: accounts[4]});
		const addresswociJtw = accounts[1]
		const addresszctTSVX = accounts[1]
		const uint256dvDPvoy = await Rootkit_financexifbpsk.balanceOf.call(addresswociJtw, {from: accounts[2]});
		const uint256rvkntjZ = await Rootkit_financexifbpsk.balanceOf.call(addresszctTSVX, {from: accounts[5]});
		const uint256ERhwy42 = await Rootkit_financexifbpsk.totalSupply.call({from: accounts[2]});

		assert.equal(uint256ERhwy42, BigInt("10000000000000000000000"))
		assert.equal(uint256dvDPvoy, BigInt("0"))
		assert.equal(uint256rvkntjZ, BigInt("0"))
	});

	it('test for Rootkit_finance', async () => {
		const Rootkit_financeTeeJHw4 = await Rootkit_finance.new({from: accounts[0]});
		const addressKthvVG7 = accounts[4]
		const uintIm85kk = BigInt("909")
		const addressT6CxvaC = accounts[4]
		const addressejLB35R = accounts[5]
		const addressTs2GZcp = accounts[0]
		const addressatPwP10 = accounts[2]
		const addressms2Pll = "0x0000000000000000000000000000000000000001"
		const uint256bnWZMzN = await Rootkit_financeTeeJHw4.balanceOf.call(addressKthvVG7, {from: accounts[1]});
		const booliXXZblN = await Rootkit_financeTeeJHw4.transferFrom.call(addressejLB35R, addressT6CxvaC, uintIm85kk, {from: accounts[1]});
		const uintd9SfLm7 = await Rootkit_financeTeeJHw4.allowance.call(addressatPwP10, addressTs2GZcp, {from: accounts[3]});
		const uint256s7f9HJR = await Rootkit_financeTeeJHw4.totalSupply.call({from: accounts[0]});
		const uint256NtVUPzh = await Rootkit_financeTeeJHw4.balanceOf.call(addressms2Pll, {from: accounts[4]});
		const uint256tQic9Nd = await Rootkit_financeTeeJHw4.totalSupply.call({from: accounts[3]});

		assert.equal(booliXXZblN, false)
		assert.equal(uint256NtVUPzh, BigInt("0"))
		assert.equal(uint256bnWZMzN, BigInt("0"))
		assert.equal(uint256s7f9HJR, BigInt("10000000000000000000000"))
		assert.equal(uint256tQic9Nd, BigInt("10000000000000000000000"))
		assert.equal(uintd9SfLm7, BigInt("0"))
	});

	it('test for Rootkit_finance', async () => {
		const Rootkit_financepiNGe2S = await Rootkit_finance.new({from: accounts[5]});
		const uint0tdPqn = BigInt("201")
		const addressxTE7R8t = accounts[0]
		const uintIHR0dh = BigInt("1603")
		const addressuAIwwIw = accounts[0]
		const addresskMimXzs = accounts[3]
		const addressRTIAX8y = accounts[3]
		const addressATpB0We = accounts[5]
		const addressg9rk0oT = accounts[2]
		const addressuW7L5o5 = accounts[5]
		const booloOZiQ3c = await Rootkit_financepiNGe2S.approve.call(addressxTE7R8t, uint0tdPqn, {from: accounts[1]});
		const boolvXCNfz9 = await Rootkit_financepiNGe2S.transferFrom.call(addresskMimXzs, addressuAIwwIw, uintIHR0dh, {from: accounts[1]});
		const uintGMvC2DS = await Rootkit_financepiNGe2S.allowance.call(addressATpB0We, addressRTIAX8y, {from: accounts[3]});
		const uint256Oy39Ns4 = await Rootkit_financepiNGe2S.balanceOf.call(addressg9rk0oT, {from: accounts[0]});
		const uint256zUU6vfG = await Rootkit_financepiNGe2S.balanceOf.call(addressuW7L5o5, {from: accounts[3]});

		assert.equal(booloOZiQ3c, true)
		assert.equal(boolvXCNfz9, false)
		assert.equal(uint256Oy39Ns4, BigInt("0"))
		assert.equal(uint256zUU6vfG, BigInt("10000000000000000000000"))
		assert.equal(uintGMvC2DS, BigInt("0"))
	});

	it('test for Rootkit_finance', async () => {
		const Rootkit_financegcLWKwY = await Rootkit_finance.new({from: accounts[4]});
		const uintXLAaXnL = BigInt("658")
		const addressS2022t2 = accounts[0]
		const uinth5zUxbl = BigInt("634")
		const addressDacFxK = accounts[4]
		const addressqFDSKAD = accounts[1]
		const uintdaVz8L5 = BigInt("625")
		const addressLJxfJJ = "0x0000000000000000000000000000000000000001"
		const uinte9NYzef = BigInt("482")
		const addressFmlR1pN = accounts[2]
		const addressqhEwB4 = accounts[0]
		const boolH5gwl9i = await Rootkit_financegcLWKwY.approve.call(addressS2022t2, uintXLAaXnL, {from: accounts[5]});
		const booluU9dJh = await Rootkit_financegcLWKwY.transferFrom.call(addressqFDSKAD, addressDacFxK, uinth5zUxbl, {from: accounts[3]});
		const boolhZLmUyc = await Rootkit_financegcLWKwY.transfer.call(addressLJxfJJ, uintdaVz8L5, {from: accounts[3]});
		const boolH2bathB = await Rootkit_financegcLWKwY.transferFrom.call(addressqhEwB4, addressFmlR1pN, uinte9NYzef, {from: accounts[3]});

		assert.equal(boolH2bathB, false)
		assert.equal(boolH5gwl9i, true)
		assert.equal(boolhZLmUyc, false)
		assert.equal(booluU9dJh, false)
	});
})